import turtle as t
def settings(t):
    t.speed(100)
    t.pensize(6)
    t.setup(600, 600)
    t.title("Art")
settings(t)
t.goto(0, 0)
t.circle(36)
t.goto(0, -36)
t.pendown()
t.goto(0, -72)
t.setheading(0)
t.setheading(240)
t.forward(42)
t.backward(42)
t.setheading(300)
t.forward(42)
t.backward(42)
t.setheading(90)
t.goto(0, -10)
t.setheading(240)
t.forward(42)
t.backward(42)
t.setheading(300)
t.forward(42)
t.backward(42)
t.mainloop()